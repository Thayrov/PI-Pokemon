const app = require('./app.js');
const {conn} = require('./config/db.config.js');
const {PORT} = require('./config/env.config');

conn
  .sync({force: true})
  .then(() => {
    app.listen(PORT, () => {
      console.log(`Server raised in: http://localhost:${PORT}`);
    });
  })
  .catch(err => {
    console.error(err);
  });
